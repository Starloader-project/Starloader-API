package de.geolykt.starloader.api.gui.screen;

/**
 * Represents a relative position within a container-like parent object.
 * This parent object might be the user screen or another {@link Screen} object.
 */
public enum RelativePosition {
    TOP_LEFT,
    TOP,
    TOP_RIGHT,
    RIGHT,
    BTTOM,
    BOTTOM_RIGHT,
    CENTER,
    UNDEFINED;
}
